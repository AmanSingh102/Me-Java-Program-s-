public class Me_17_Beautifull_Table
{
    public static void main(String[]args)
    {
        int t= 10;
        for(int i = 1; i <= t; i++)
        {
           for(int j= 1;j<=t; j++)
           {
            System.out.print((i*j) + "\t");
           } 
           System.out.println();
        }
    }
    
}
